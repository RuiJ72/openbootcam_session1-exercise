package openbootcam.session1.exerises.com;

public class Types {

    public static void main(String[] args) {

        // Main Data Types

        // Integer Numerics
        int integer = 200;
        System.out.println("Example of an integer: " + integer);

        float floating = 25.5f;
        System.out.println("A float type with de value: " + floating);

        double doubleNumber = 205.5d;
        System.out.println("A decimal double type with de value: " + doubleNumber);

        long longNumber = 1896525;
        System.out.println("Type long example: " + longNumber);

        boolean choiceTrue = true;
        System.out.println("A boolean type boolean: " + choiceTrue);

        boolean choiceFalse = false;
        System.out.println("A boolean type boolean, false: " + choiceFalse);

        String name = "Rui Jorge Lagos";
        int age = 51;
        System.out.println("My name is: " + name + " and I have  " + age + " years old!");


    }
}
